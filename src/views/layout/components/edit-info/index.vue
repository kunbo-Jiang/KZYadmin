<template>
  <el-dialog
    width="500px"
    title="编辑"
    v-model="visible"
    :close-on-click-modal="false"
    @closed="dialogClosedHandle">
    <el-form
      :model="form"
      :rules="rules"
      ref="refForm"
      @keyup.enter="submit()"
      label-position="top">
      <el-form-item label="昵称" prop="name">
        <el-input v-model="user.name" placeholder="昵称" />
      </el-form-item>
      <el-form-item label="手机号" prop="phone">
        <el-input v-model="user.phone" placeholder="手机号" />
      </el-form-item>
      <el-form-item label="原密码" prop="oldPassword">
        <el-input v-model="form.oldPassword" placeholder="原密码" show-password />
      </el-form-item>
      <el-form-item label="新密码" prop="newPassword">
        <el-input v-model="form.newPassword" placeholder="新密码" show-password />
      </el-form-item>
      <el-form-item label="确认密码" prop="confirmPassword">
        <el-input v-model="form.confirmPassword" placeholder="确认密码" show-password />
      </el-form-item>
    </el-form>
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="visible = false">取消</el-button>
        <el-button v-repeat type="primary" @click="submit()">确认</el-button>
      </span>
    </template>
  </el-dialog>
</template>

<script>
import { computed, defineComponent, reactive, ref } from 'vue'
import { useRouter } from 'vue-router'
import { useStore } from 'vuex'

import { ElMessage } from 'element-plus'

import { isEmail, isMobile } from '@/utils/regular'

export default defineComponent({
  setup() {
    const router = useRouter()
    const store = useStore()

    const user = computed(() => store.state.user.user)

    const visible = ref(false)
    const refForm = ref()
    const form = reactive({
      name: '',
      phone: '',
      oldPassword: '',
      newPassword: '',
      confirmPassword: ''
    })
    const rules = reactive(function () {
      const checkMobile = (_rule, value, callback) => {
        if (value !== '' && !isMobile(value)) {
          callback(new Error('请输入正确的手机号'))
        } else {
          callback()
        }
      }
      const checkEmail = (_rule, value, callback) => {
        if (value !== '' && !isEmail(value)) {
          callback(new Error('请输入正确的邮箱'))
        } else {
          callback()
        }
      }
      const checkOldPassword = (_rule, value, callback) => {
        if (form.newPassword !== '' && value === '') {
          callback(new Error('请输入原密码'))
        } else {
          callback()
        }
      }
      const checkConfirmPassword = (_rule, value, callback) => {
        if (form.newPassword !== value) {
          callback(new Error('新密码与确认密码不一致'))
        } else {
          callback()
        }
      }
      const rule = {
        nickname: [{ required: true, message: '请输入昵称', trigger: 'blur' }],
        mobile: [{ validator: checkMobile, trigger: 'blur' }],
        email: [{ validator: checkEmail, trigger: 'blur' }],
        oldPassword: [{ validator: checkOldPassword, trigger: 'blur' }],
        confirmPassword: [{ validator: checkConfirmPassword, trigger: 'blur' }]
      }
      return rule
    }())

    /**
     * @description: 表单验证提交
     * @param {*}
     * @return {*}
     */
    const submit = () => {
      refForm.value.validate(valid => {
        if (valid) {
          const params = {
            nickname: form.nickname,
            mobile: form.mobile,
            email: form.email,
            old_password: form.oldPassword,
            new_password: form.newPassword
          }
          store.dispatch('user/editUser', params).then(r => {
            if (r) {
              visible.value = false
              ElMessage({
                message: '操作成功!',
                type: 'success',
                onClose: () => {
                  if (r.data === 1) {
                    router.replace({ name: 'login', query: {
                      targetUrl: encodeURIComponent(location.href)
                    } })
                  }
                }
              })
            }
          })
        }
      })
    }

    /**
      * @description: 弹窗关闭动画结束时的回调
      * @param {*}
      * @return {*}
      */
    const dialogClosedHandle = () => {
      refForm.value.resetFields()
    }

    const init = () => {
      visible.value = true
      form.nickname = user.value.nickname
      form.mobile = user.value.mobile
      form.email = user.value.email
    }

    return {
      user,
      refForm,
      visible,
      form,
      rules,
      init,
      submit,
      dialogClosedHandle
    }
  }
})
</script>
